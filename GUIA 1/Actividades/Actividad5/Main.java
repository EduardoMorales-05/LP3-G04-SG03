public class Main {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5}; // Declaración con tipo
        
        // Imprimir
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
    }
}


